<p>Grey heron The grey heron (Ardea cinerea) is a long-legged predatory wading bird of the heron family, Ardeidae, native throughout temperate</p>

<p>&nbsp;</p>

<p>Europe and Asia and also parts of Africa. It is resident<img alt="" src="http://www.w3schools.com/html/pic_mountain.jpg" /> in much of its range, but some</p>

<p><img alt="" src="http://www.w3schools.com/html/pic_mountain.jpg" style="width: 304px; height: 228px;" /></p>

<p>populations from the more northern parts migrate southwards in autumn.</p>

<p><img alt="" src="http://www.w3schools.com/html/pic_mountain.jpg" style="width: 304px; height: 228px;" /></p>

<p>A bird of wetland areas, it can be seen around lakes, rivers, ponds, marshes and on the sea coast. It feeds mostly on aquatic creatures which it catches after standing stationary beside or in the water or stalking its prey through the shallows. Photograph: JJ Harrison Recently featured: Scelophysa trimeni Lanterns of the Dead Dale D. Myers</p>
